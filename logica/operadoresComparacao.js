/*
Operadores de comparação

> - maior que
>= - maior ou igual a
< - menor que
<= - menor que ou igual a
== - igualdade 
=== - igualdade estrita
!= - diferente
!== - diferenca estrita
*/

console.log(10 > 5);
console.log(10 >= 5);
console.log(10 <= 11);
console.log(11 == "11");
console.log(11 === "11");
console.log(11 != "11");
console.log(11 !== "11");
